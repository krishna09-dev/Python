'''Write a program that asks the user to input the radius of a circle. The program
should calculate the area of the circle ï¿½ = pi*r^2 and print it out. '''

# Prompt the user to enter the radius of the circle
radius = float(input("Enter the radius of the circle: "))

# Calculate the area of the circle using the formula: area = pi * radius^2
area = 3.14159 * radius ** 2

# Print out the calculated area of the circle
print("The area of the circle is:", area)

